#include "libft.h"

size_t	ft_strlenchr(const char *s, char c)
{
	size_t i;

	i = 0;
	while (*s == c && *s)
		s++;
	while (*s != c && *s)
	{
		i++;
		s++;
	}
	return (i);
}
